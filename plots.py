# Plot
def plot_accuracy(accuracies):
    """
    Plots accuracy vs round.

    Parameters:
        accuracies (list of float): Accuracy values (in percentage)
    """
    rounds = list(range(1, len(accuracies) + 1))

    plt.figure()
    plt.plot(rounds, accuracies)

    plt.xlabel("Round")
    plt.ylabel("Accuracy (%)")
    plt.ylim(0, 100)

    plt.grid()
    plt.subplots_adjust(left=0.1, right=0.95, top=0.95, bottom=0.1)
    plt.margins(x=0, y=0)
    plt.show()

#plot_accuracy(kde_cifar_accuracies)

def plot_method_comparison(cifar_acc, gstrb_acc, title):
    rounds = list(range(1, len(cifar_acc) + 1))

    plt.figure(figsize=(8, 5))

    # Plot both lines
    plt.plot(rounds, cifar_acc, label="CIFAR", linewidth=2)
    plt.plot(rounds, gstrb_acc, label="GTSRB", linewidth=2)

    # Labels & styling
    plt.xlabel("Round")
    plt.ylabel("Accuracy (%)")
    plt.title(title)
    plt.ylim(0, 100)
    plt.legend()
    plt.grid()

    plt.tight_layout()
    plt.show()


